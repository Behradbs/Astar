grid = [
    "S..............G",
    "................",
    "..PPPP..........",
    "..P..P..........",
    "..P..P...F......",
    "................",
    ".......B........",
    ".......T........",
    ".......F........",
    ".......RR.......",
    "..C.............",
    "................",
    "................",
    "................",
    "................",
    "..............G",
]

color_map = {
    'S': (255, 255, 255),
    'G': (255, 255, 0),
    'B': (0, 128, 0),
    'T': (139, 69, 19),
    'P': (0, 0, 255),
    'F': (255, 0, 255),
    'R': (128, 128, 128),
    'C': (165, 42, 42),
    '#': (255, 0, 0),
    '.': (0, 255, 0),
}

def colorize(ch):
    r, g, b = color_map.get(ch, (0, 255, 0))
    return f"\033[48;2;{r};{g};{b}m {ch} \033[0m"

def display():
    for row in grid:
        print(''.join(colorize(c) for c in row))

if __name__ == "__main__":
    display()
